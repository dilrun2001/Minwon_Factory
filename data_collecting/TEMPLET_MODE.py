# 📋 공무원 조치 기반 회신문 템플릿 생성기
def run_template_moin_reply(moin_summary, 조치1, 조치2, 부서="담당 부서", 전화="051-000-0000", model=None):
    """
    공무원이 적은 조치 요약을 바탕으로 정중한 회신문 생성
    - moin_summary: 민원 요약
    - 조치1, 조치2: 공무원이 입력한 조치 내용
    - 부서, 전화: 회신 마지막 안내부 정보
    - model: 텍스트 생성기 모델 (없으면 prompt만 출력)
    """
    prompt = f"""다음 정보를 바탕으로 정중한 회신문을 작성하세요.

민원 요약: {moin_summary}
조치1: {조치1}
조치2: {조치2}

출력 형식:
1. 안녕하십니까? 귀하께서 신청하신 민원에 대한 검토 결과를 다음과 같이 알려드립니다.

2. 귀하의 민원 내용은 '{moin_summary}'에 관한 것으로 이해됩니다.

3. 검토 결과 및 조치 사항은 다음과 같습니다.
- {조치1}
- {조치2}

4. 기타 궁금하신 사항은 {부서}({전화})로 연락 주시기 바랍니다. 감사합니다.
"""
    return prompt if model is None else model(prompt)

# ✅ 테스트 예시
test2 = run_template_moin_reply(
    moin_summary="도로변 방치 쓰레기 관련 민원",
    조치1="환경미화원이 현장 수거 완료",
    조치2="주기적 순찰 실시 예정",
    부서="자원순환과",
    전화="051-220-4434"
)
print(test2)